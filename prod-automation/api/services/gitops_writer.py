    def _resolve_branch(self) -> str:
        addons = getattr(self.config, "addons", None)
        argocd = getattr(addons, "argocd", None) if addons else None
        repo = getattr(argocd, "repository", None) if argocd else None
        if repo and getattr(repo, "branch", None):
            return repo.branch
        return "main"

    def _get_output(self, key: str) -> str:
        value = self.outputs.get(key)
        if not value:
            raise ValueError(f"Required Pulumi output '{key}' not found")
        return value

    def _customer_host(self, subdomain: str) -> str:
        return f"{subdomain}.{self.domain}"

