    def push_to_github(self) -> None:
        values_files = self.generate_customer_values()
        app_files = self.generate_customer_apps()
        all_files = {**values_files, **app_files}

        if not all_files:
            return

        token = settings.github_pat
        repo = settings.github_repo

        if not token or not repo:
            raise ValueError("GitHub PAT and repository must be configured in settings")

        owner, name = repo.split("/", 1)
        base_url = "https://api.github.com"
        headers = {
            "Authorization": f"token {token}",
            "Accept": "application/vnd.github+json",
        }

        ref_resp = requests.get(
            f"{base_url}/repos/{owner}/{name}/git/ref/heads/{self.branch}",
            headers=headers,
            timeout=10,
        )
        ref_resp.raise_for_status()
        ref_data = ref_resp.json()
        latest_commit_sha = ref_data["object"]["sha"]

        commit_resp = requests.get(
            f"{base_url}/repos/{owner}/{name}/git/commits/{latest_commit_sha}",
            headers=headers,
            timeout=10,
        )
        commit_resp.raise_for_status()
        commit_data = commit_resp.json()
        base_tree_sha = commit_data["tree"]["sha"]

        blobs: Dict[str, str] = {}
        for path, content in all_files.items():
            blob_resp = requests.post(
                f"{base_url}/repos/{owner}/{name}/git/blobs",
                headers=headers,
                json={
                    "content": content,
                    "encoding": "utf-8",
                },
                timeout=10,
            )
            blob_resp.raise_for_status()
            blobs[path] = blob_resp.json()["sha"]

        tree_entries = [
            {
                "path": path,
                "mode": "100644",
                "type": "blob",
                "sha": sha,
            }
            for path, sha in blobs.items()
        ]

        tree_resp = requests.post(
            f"{base_url}/repos/{owner}/{name}/git/trees",
            headers=headers,
            json={
                "base_tree": base_tree_sha,
                "tree": tree_entries,
            },
            timeout=10,
        )
        tree_resp.raise_for_status()
        new_tree_sha = tree_resp.json()["sha"]

        commit_message = f"chore: generate GitOps values for {self.customer_id}"
        new_commit_resp = requests.post(
            f"{base_url}/repos/{owner}/{name}/git/commits",
            headers=headers,
            json={
                "message": commit_message,
                "tree": new_tree_sha,
                "parents": [latest_commit_sha],
            },
            timeout=10,
        )
        new_commit_resp.raise_for_status()
        new_commit_sha = new_commit_resp.json()["sha"]

        update_ref_resp = requests.patch(
            f"{base_url}/repos/{owner}/{name}/git/refs/heads/{self.branch}",
            headers=headers,
            json={"sha": new_commit_sha, "force": False},
            timeout=10,
        )
        update_ref_resp.raise_for_status()

